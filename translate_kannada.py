def translate_hindi_to_kannada(text):
    """Translate Hindi text to Kannada"""
    if not isinstance(text, str):
        return text
    
    result = text
    for hindi, kannada in translations.items():
        result = result.replace(hindi, kannada)
    
    return result

def process_dict(data):
    """Recursively process dictionary and translate values"""
    if isinstance(data, dict):
        return {key: process_dict(value) for key, value in data.items()}
    elif isinstance(data, list):
        return [process_dict(item) for item in data]
    elif isinstance(data, str):
        return translate_hindi_to_kannada(data)
    else:
        return data

# Process the Hindi data
